    private void viewnotificationsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewnotificationsActionPerformed

        // TODO add your handling code here:
         java.util.List<String> notifs = NotificationViewer.getNotifications("student");
    if (notifs.isEmpty()) {
        JOptionPane.showMessageDialog(this, "No notifications for you.");
        return;
    }
    JTextArea area = new JTextArea(String.join("\n\n", notifs));
    area.setEditable(false);
    JScrollPane scroll = new JScrollPane(area);
    scroll.setPreferredSize(new Dimension(400, 300));
    JOptionPane.showMessageDialog(
        this,
        scroll,
        "Notifications for Student",
        JOptionPane.INFORMATION_MESSAGE
    );
    }//GEN-LAST:event_viewnotificationsActionPerformed
    
